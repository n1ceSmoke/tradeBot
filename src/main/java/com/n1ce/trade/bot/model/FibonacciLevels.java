package com.n1ce.trade.bot.model;

public class FibonacciLevels {
	private final double level618;
	private final double level786;

	public FibonacciLevels(double level618, double level786) {
		this.level618 = level618;
		this.level786 = level786;
	}

	public double getLevel618() {
		return level618;
	}

	public double getLevel786() {
		return level786;
	}
}